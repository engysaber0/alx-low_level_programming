#include "3-calc.h"
/**
 * op_add - add 2 int
 * @a: first int
 * @b: second int
 * Return: result
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - sub of 2 int
 * @a: first int
 * @b: second int
 * Return: difference
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - mul 2 int
 * @a: first int
 * @b: second int
 * Return: mul
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - div 2 int
 * @a: first int
 * @b: second int
 * Return: div
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - mod 2 int
 * @a: first int
 * @b: second int
 * Return: mod
*/
int op_mod(int a, int b)
{
return (a % b);
}
